<script setup lang="ts">
import { ref, computed, watch, onMounted, nextTick } from 'vue'
import { useI18n } from 'vue-i18n'
import Input from '@/components/ui/Input.vue'
import Combobox from '@/components/ui/Combobox.vue'
import CustomSelect from '@/components/ui/CustomSelect.vue'
import DatePicker from '@/components/ui/DatePicker.vue'
import TimePicker from '@/components/ui/TimePicker.vue'
import CouncilCommitteesService from '@/services/CouncilCommitteesService'
import LookupsService from '@/services/LookupsService'

const { t } = useI18n()

export interface MeetingData {
  id?: number
  title: string | null
  isCommittee: boolean
  committeeId: string | null
  committeeName?: string | null
  committeeTypeId?: string | null
  date: string | null
  startTime: string
  endTime: string
  location: string | null
  notes: string | null
  typeId: string | null
  url: string | null
  councilSessionId: string | null
  referenceNumber: string | null
}

interface Props {
  modelValue: MeetingData
  viewMode?: boolean
}

const props = withDefaults(defineProps<Props>(), {
  viewMode: false
})

const emit = defineEmits<{
  'update:modelValue': [value: MeetingData]
}>()

// Local state
const localMeeting = ref<MeetingData>({ ...props.modelValue })
const committees = ref<Array<{ id: string; name: string; label: string; typeId?: string }>>([])
const councilSessions = ref<Array<{ id: string; name: string }>>([])
const meetingTypes = ref<Array<{ id: string; name: string; code?: string }>>([])
const errors = ref<Record<string, string>>({})

// Flag to prevent infinite loop between watchers
let isUpdatingFromProps = false

// Computed
// Check if user has permission to create meetings for any committee/council
const hasCommitteePermission = computed(() => {
  return committees.value.length > 0
})

const showCouncilSession = computed(() => {
  // Show council session for specific committee types
  const selectedCommittee = committees.value.find(c => c.id === localMeeting.value.committeeId)
  return selectedCommittee?.typeId === 'council' // Adjust based on actual type
})

// Helper to check if a type is remote
const isTypeRemote = (type: { id: string; name: string; code?: string } | null | undefined): boolean => {
  if (!type) return false
  return type.code === 'remote' || type.name?.toLowerCase().includes('عن بعد') || type.name?.toLowerCase().includes('remote')
}

const isRemoteMeeting = computed(() => {
  const selectedType = meetingTypes.value.find(t => String(t.id) === String(localMeeting.value.typeId))
  return isTypeRemote(selectedType)
})

// Watch for external changes
watch(() => props.modelValue, (newVal) => {
  if (JSON.stringify(newVal) !== JSON.stringify(localMeeting.value)) {
    isUpdatingFromProps = true
    localMeeting.value = { ...newVal }
    nextTick(() => {
      isUpdatingFromProps = false
    })
  }
}, { deep: true })

// Watch for local changes and emit
watch(localMeeting, (newVal) => {
  if (!isUpdatingFromProps) {
    emit('update:modelValue', { ...newVal })
  }
}, { deep: true, flush: 'sync' })

// Watch start/end time for real-time validation
watch(
  () => [localMeeting.value.startTime, localMeeting.value.endTime],
  ([startTime, endTime]) => {
    if (startTime && endTime) {
      const start = startTime.split(':').map(Number)
      const end = endTime.split(':').map(Number)
      const startMinutes = start[0] * 60 + start[1]
      const endMinutes = end[0] * 60 + end[1]
      if (endMinutes <= startMinutes) {
        errors.value.endTime = t('EndTimeMustBeAfterStart')
      } else {
        delete errors.value.endTime
      }
    }
  }
)

// Methods
const setCommittee = (value: boolean) => {
  localMeeting.value.isCommittee = value
  if (!value) {
    localMeeting.value.committeeId = null
    localMeeting.value.councilSessionId = null
  }
}

const onCommitteeChange = (committeeId: string) => {
  const committee = committees.value.find(c => c.id === committeeId)
  if (committee) {
    localMeeting.value.committeeName = committee.name
    localMeeting.value.committeeTypeId = committee.typeId
  }
}

const loadCommittees = async () => {
  try {
    const response = await CouncilCommitteesService.listUserCommitteesForMeeting()
    const data = Array.isArray(response) ? response : (response?.data || [])
    committees.value = data.map((c: any) => ({
      id: c.id,
      name: c.name,
      label: c.name,
      typeId: c.typeId
    }))

    // If no committees available (no permission), ensure personal meeting mode
    if (committees.value.length === 0 && localMeeting.value.isCommittee) {
      localMeeting.value.isCommittee = false
      localMeeting.value.committeeId = null
      localMeeting.value.councilSessionId = null
    }
  } catch (error) {
    console.error('Failed to load committees:', error)
    // On error, ensure personal meeting mode as fallback
    if (localMeeting.value.isCommittee) {
      localMeeting.value.isCommittee = false
      localMeeting.value.committeeId = null
    }
  }
}

const loadMeetingTypes = async () => {
  try {
    const response = await LookupsService.getMeetingTypes()
    const data = Array.isArray(response) ? response : (response?.data || [])
    meetingTypes.value = data.filter((t: any) => t && t.id)
  } catch (error) {
    console.error('Failed to load meeting types:', error)
  }
}

const loadCouncilSessions = async () => {
  try {
    const response = await LookupsService.getCouncilSessions()
    councilSessions.value = response || []
  } catch (error) {
    console.error('Failed to load council sessions:', error)
  }
}

// Validation
const validate = (): boolean => {
  errors.value = {}

  if (!localMeeting.value.title?.trim()) {
    errors.value.title = t('SubjectRequired')
  }

  if (!localMeeting.value.location?.trim()) {
    errors.value.location = t('LocationRequired')
  }

  if (!localMeeting.value.date) {
    errors.value.date = t('DateRequired')
  }

  if (!localMeeting.value.typeId) {
    errors.value.typeId = t('MeetingTypeRequired')
  }

  if (isRemoteMeeting.value && !localMeeting.value.url?.trim()) {
    errors.value.url = t('MeetingUrlRequired')
  }

  // Validate end time is after start time
  if (localMeeting.value.startTime && localMeeting.value.endTime) {
    const start = localMeeting.value.startTime.split(':').map(Number)
    const end = localMeeting.value.endTime.split(':').map(Number)
    const startMinutes = start[0] * 60 + start[1]
    const endMinutes = end[0] * 60 + end[1]
    if (endMinutes <= startMinutes) {
      errors.value.endTime = t('EndTimeMustBeAfterStart')
    }
  }

  return Object.keys(errors.value).length === 0
}

// Expose validate method
defineExpose({ validate })

// Lifecycle
onMounted(() => {
  loadCommittees()
  loadMeetingTypes()
  loadCouncilSessions()
})
</script>

