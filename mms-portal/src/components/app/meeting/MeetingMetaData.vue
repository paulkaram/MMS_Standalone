          :disabled="viewMode"
        ></textarea>
      </div>
    </div>

    <!-- Row 2: Related Committee & Reference -->
    <div class="form-row" :class="localMeeting.isCommittee ? 'grid-cols-3' : (hasCommitteePermission ? 'grid-cols-2' : 'grid-cols-1')">
      <!-- Only show toggle if user has permission to create committee meetings -->
      <div class="form-group" v-if="hasCommitteePermission">
        <label class="form-label">
          {{ $t('RelatedCommittee') }}
        </label>
        <div class="toggle-group">
          <button
            type="button"
            :class="['toggle-btn', !localMeeting.isCommittee ? 'active' : '']"
            :disabled="viewMode"
            @click="setCommittee(false)"
          >
            {{ $t('PersonalMeeting') }}
          </button>
          <button
            type="button"
            :class="['toggle-btn', localMeeting.isCommittee ? 'active' : '']"
            :disabled="viewMode"
            @click="setCommittee(true)"
          >
            {{ $t('CommitteeMeeting') }}
          </button>
        </div>
      </div>

      <div class="form-group" v-if="localMeeting.isCommittee">
        <label class="form-label">
          {{ $t('Committee') }}
          <span class="required">*</span>
        </label>
        <Combobox
          v-model="localMeeting.committeeId"
          :options="committees"
          item-value="id"
          item-text="label"
          :placeholder="$t('SelectCommittee')"
          :disabled="viewMode"
          @update:model-value="onCommitteeChange"
        />
      </div>

      <div class="form-group">
        <label class="form-label">
          {{ $t('ReferenceNumber') }}
        </label>
        <Input
          v-model="localMeeting.referenceNumber"
