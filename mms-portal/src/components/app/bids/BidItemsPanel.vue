<script setup lang="ts">
import { ref, computed, onMounted } from 'vue'
import Icon from '@/components/ui/Icon.vue'
import Modal from '@/components/ui/Modal.vue'
import ConfirmModal from '@/components/ui/ConfirmModal.vue'
import DatePicker from '@/components/ui/DatePicker.vue'
import CustomSelect from '@/components/ui/CustomSelect.vue'
import TextEditor from '@/components/ui/TextEditor.vue'
import TagPicker from '@/components/ui/TagPicker.vue'
import BidsService, { type BidItem, type BidItemPost } from '@/services/BidsService'
import { useToast } from '@/composables/useToast'
import { useI18n } from 'vue-i18n'

interface Props { bidId: number; items: BidItem[]; canEdit: boolean }
const props = defineProps<Props>()
const emit = defineEmits<{ changed: [] }>()

const { t } = useI18n()
const { toast } = useToast()

const modalOpen = ref(false)
const saving = ref(false)
const form = ref<{ id?: number } & BidItemPost>(emptyForm())

// Lookup data for the item-type and related-item pickers.
// Classification (BidItemType §5.11) was removed from this form per user request —
// tags fill that slot. Procurement-specific classification will be set in the
// procurement UI (#30) where it actually applies.
const itemTypeOptions = ref<{ id: string; name: string }[]>([])
const relatableItemOptions = ref<{ id: string; name: string }[]>([])

function emptyForm(): BidItemPost {
  return {
    referenceNumber: '',
    externalReferenceNumber: null,
    content: '',
    internalNote: null,
    order: (props.items?.length || 0) + 1,
    dueDate: null,
    bidItemTypeId: null,
    itemTypeId: null,
    relatedItemId: null,
    tagIds: []
  }
}

/**
 * Live form validity — drives the Save button's disabled state instead of
 * showing red borders. Strips HTML tags so an "empty" TipTap doc (`<p></p>`)
 * is correctly treated as missing.
 */
const isFormValid = computed(() => {
  const stripped = (form.value.content || '').replace(/<[^>]+>/g, '').trim()
  return !!stripped && !!form.value.itemTypeId && !!form.value.dueDate
})

const sortedItems = computed(() => [...props.items].sort((a, b) => a.order - b.order || a.id - b.id))

function openAddModal() { form.value = emptyForm(); modalOpen.value = true }
function openEditModal(item: BidItem) {
  form.value = {
    id: item.id,
    referenceNumber: item.referenceNumber,
    externalReferenceNumber: item.externalReferenceNumber,
    content: item.content,
    internalNote: item.internalNote,
    order: item.order,
    dueDate: item.dueDate || null,
    bidItemTypeId: item.bidItemTypeId || null,
    itemTypeId: item.itemTypeId || null,
    relatedItemId: item.relatedItemId || null,
    tagIds: (item.tagList || []).map(t => Number(t.id)).filter(n => !isNaN(n))
  }
  modalOpen.value = true
}

async function loadLookups() {
  try {
    const [types, relatable]: any = await Promise.all([
      BidsService.listItemTypes().catch(() => []),
      BidsService.listRelatableItems(props.bidId).catch(() => [])
    ])
    itemTypeOptions.value = (types?.data ?? types ?? []).map((t: any) => ({ id: String(t.id), name: t.name }))
    relatableItemOptions.value = (relatable?.data ?? relatable ?? []).map((r: any) => ({ id: String(r.id), name: r.name }))
  } catch { /* fail-silent — pickers just show empty */ }
}

onMounted(loadLookups)

async function save() {
  if (!isFormValid.value) return
  saving.value = true
  try {
    const { id, ...payload } = form.value
    if (id) await BidsService.updateItem(id, payload)
    else await BidsService.addItem(props.bidId, payload)
    toast.success(t('Saved') || 'Saved')
    modalOpen.value = false
    emit('changed')
  } catch (err: any) {
    toast.error(err?.response?.data?.message || t('ErrorOccured'))
  } finally {
    saving.value = false
  }
}

function formatDate(iso: string) { return new Date(iso).toLocaleDateString() }
function isOverdue(iso: string): boolean { return new Date(iso).getTime() < Date.now() }

// Delete uses the styled ConfirmModal instead of native browser confirm()
const confirmOpen = ref(false)
const deleting = ref(false)
const pendingDelete = ref<BidItem | null>(null)

function confirmDelete(item: BidItem) {
  pendingDelete.value = item
  confirmOpen.value = true
}

async function performDelete() {
  if (!pendingDelete.value) return
  deleting.value = true
  try {
    await BidsService.deleteItem(pendingDelete.value.id)
    toast.success(t('Deleted') || 'Deleted')
    confirmOpen.value = false
    pendingDelete.value = null
    emit('changed')
  } catch (err: any) {
    toast.error(err?.response?.data?.message || t('ErrorOccured'))
  } finally {
    deleting.value = false
  }
}
</script>

