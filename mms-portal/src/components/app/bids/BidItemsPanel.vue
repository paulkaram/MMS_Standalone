      <Icon icon="mdi:format-list-bulleted" class="w-8 h-8" />
      <p>{{ $t('NoBidItems') || 'No items yet. Add one to start the visions cycle.' }}</p>
    </div>

    <div v-else class="bip-list">
      <article v-for="item in sortedItems" :key="item.id" class="bip-item">
        <div class="bip-item-head">
          <span class="bip-ref"><Icon icon="mdi:pound" class="w-3 h-3" />{{ item.referenceNumber }}</span>
          <span v-if="item.externalReferenceNumber" class="bip-ref-ext">{{ item.externalReferenceNumber }}</span>
          <span class="bip-order">#{{ item.order }}</span>
          <div v-if="canEdit" class="bip-item-actions">
            <button class="icon-btn" @click="openEditModal(item)" :title="$t('Edit')"><Icon icon="mdi:pencil-outline" class="w-4 h-4" /></button>
            <button class="icon-btn icon-btn-danger" @click="confirmDelete(item)" :title="$t('Delete')"><Icon icon="mdi:delete-outline" class="w-4 h-4" /></button>
          </div>
        </div>
        <div class="bip-content" v-html="item.content"></div>
        <div class="bip-item-meta">
          <span v-if="item.dueDate" class="bip-due" :class="{ overdue: isOverdue(item.dueDate) }">
            <Icon icon="mdi:calendar-clock" class="w-3 h-3" />
            {{ formatDate(item.dueDate) }}
            <em v-if="isOverdue(item.dueDate)" class="bip-overdue">{{ $t('Overdue') || 'Overdue' }}</em>
          </span>
          <span v-if="item.internalNote" class="bip-note">
            <Icon icon="mdi:note-outline" class="w-3 h-3" />
            {{ item.internalNote }}
          </span>
        </div>
      </article>
    </div>

    <!-- Add/Edit Modal -->
    <!-- Item modal needs room for: rich text editor, two type dropdowns, related-item picker,
         tags multi-select, dates, internal notes — `xl` (1000px) gives the editor and pickers
         enough horizontal space to feel comfortable rather than cramped. -->
    <ConfirmModal
      v-model="confirmOpen"
      :title="$t('ConfirmDeleteItem') || 'Delete item?'"
      :message="$t('ConfirmDeleteItemBody') || 'This will permanently remove the item from the bid. Continue?'"
